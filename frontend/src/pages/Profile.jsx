import React, { useState } from "react";
import { Avatar, AvatarImage } from "../components/ui/avatar";
import Navbar from "../components/shared/Navbar";
import { Contact, Mail, Pen } from "lucide-react";
import { Badge } from "../components/ui/badge";
import { Label } from "../components/ui/label";  // ADD THIS
import UpdateProfileDialog from "../components/UpdateProfileDialog";
import ApplicationList from "../components/ApplicationList";
import { useSelector } from "react-redux";

function Profile() {
  const { user } = useSelector((store) => store.auth);
  const [open, setOpen] = useState(false);

  if (!user) return null;

  return (
    <div>
      <Navbar />

      <div className="max-w-4xl mx-auto bg-white border border-gray-200 rounded-2xl my-5 p-8 shadow-lg">
        <div className="flex justify-between">
          <div className="flex items-center gap-4">
            <Avatar className="h-24 w-24">
              <AvatarImage
                src={
                  user?.profile?.profilePhoto
                    ? user.profile.profilePhoto
                    : "https://www.shutterstock.com/image-vector/circle-line-simple-design-logo-600nw-2174926871.jpg"
                }
              />
            </Avatar>

            <div>
              <h1 className="font-medium text-xl">
                {user?.fullname}
              </h1>
              <p>
                {user?.profile?.bio ? user.profile.bio : "No bio added"}
              </p>
            </div>
          </div>

          <button
            onClick={() => setOpen(true)}
            className="cursor-pointer"
          >
            <Pen />
          </button>
        </div>

        {/* Contact Info */}
        <div className="my-5">
          <div className="flex items-center gap-3 my-2">
            <Mail />
            <span>{user?.email}</span>
          </div>

          <div className="flex items-center gap-3 my-2">
            <Contact />
            <span>{user?.phoneNumber}</span>
          </div>
        </div>

        {/* Skills */}
        <div>
          <h1 className="font-semibold">Skills</h1>
          <div className="flex items-center gap-2 mt-2 flex-wrap">
            {user?.profile?.skills &&
            user.profile.skills.length > 0 ? (
              user.profile.skills.map((skill, index) => (
                <Badge key={index}>{skill}</Badge>
              ))
            ) : (
              <span>NA</span>
            )}
          </div>
        </div>

        {/* Resume */}
        <div className='grid w-full max-w-sm items-center gap-1.5 mt-5'>
          <Label className="text-md font-bold">Resume</Label>
          {
            user?.profile?.resume ? (
              <a 
                href={user?.profile?.resume} 
                download={user?.profile?.resumeOriginalName}
                className='text-blue-500 w-full hover:underline cursor-pointer'
              >
                {user?.profile?.resumeOriginalName || "Download Resume"}
              </a>
            ) : <span>NA</span>
          }
        </div>
      </div>

      {/* Applied Jobs */}
      <div className="max-w-4xl mx-auto bg-white rounded-2xl p-6">
        <h1 className="font-bold text-lg mb-4">Applied Jobs</h1>
        <ApplicationList />
      </div>

      <UpdateProfileDialog open={open} setOpen={setOpen} />
    </div>
  );
}

export default Profile;